let currentLanguage = localStorage.getItem('language') || 'en';

function setLanguage(lang) {
    currentLanguage = lang;
    localStorage.setItem('language', lang);
    applyTranslations();
}

function applyTranslations() {
    let pageType = document.body.getAttribute('data-page') || '';
    if (!pageType && document.body.classList.contains('register-page')) pageType = 'register';
    if (!pageType && document.body.classList.contains('login-page')) pageType = 'login';
    if (!pageType && document.body.classList.contains('dashboard-page')) pageType = 'dashboard';
    const pageTranslations = translations[currentLanguage][pageType];
    
    if (!pageTranslations) return;
    
    // Update all elements with data-translate attribute
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        if (pageTranslations[key]) {
            element.textContent = pageTranslations[key];
        }
    });
    
    // Update placeholders
    document.querySelectorAll('[data-translate-placeholder]').forEach(element => {
        const key = element.getAttribute('data-translate-placeholder');
        if (pageTranslations[key]) {
            element.placeholder = pageTranslations[key];
        }
    });
    
    // Update title
    if (pageType === 'login') {
        document.title = translations[currentLanguage].login.title + ' - Modern Style';
    } else if (pageType === 'register') {
        document.title = translations[currentLanguage].register.title + ' - Modern Style';
    } else if (pageType === 'dashboard') {
        document.title = translations[currentLanguage].dashboard.siteName;
    } else if (pageType === 'profile') {
        document.title = translations[currentLanguage].profile.pageTitle + ' — ' + translations[currentLanguage].profile.siteName;
    } else if (pageType === 'test') {
        document.title = translations[currentLanguage].test.pageTitle + ' — ' + translations[currentLanguage].test.siteName;
    }
    
    // Update language switcher active state (login/register pages)
    document.querySelectorAll('.lang-btn').forEach(btn => {
        if (btn.getAttribute('data-lang') === currentLanguage) {
            btn.classList.add('active');
        } else {
            btn.classList.remove('active');
        }
    });
    
    // Update language dropdown (dashboard/profile/test): trigger label + option active state
    const labelEl = document.querySelector('.lang-dropdown-label');
    if (labelEl) labelEl.textContent = currentLanguage.toUpperCase();
    document.querySelectorAll('.lang-option').forEach(btn => {
        if (btn.getAttribute('data-lang') === currentLanguage) {
            btn.classList.add('active');
        } else {
            btn.classList.remove('active');
        }
    });
}

// Initialize language dropdown (open/close, select option)
function initLangDropdown() {
    const dropdown = document.querySelector('.lang-dropdown');
    if (!dropdown) return;
    const trigger = document.getElementById('langDropdownBtn');
    const menu = document.getElementById('langDropdownMenu');
    if (!trigger || !menu) return;
    
    trigger.addEventListener('click', function(e) {
        e.stopPropagation();
        dropdown.classList.toggle('open');
        const open = dropdown.classList.contains('open');
        trigger.setAttribute('aria-expanded', open);
        menu.setAttribute('aria-hidden', !open);
    });
    
    menu.addEventListener('click', function(e) {
        e.stopPropagation();
    });
    
    dropdown.querySelectorAll('.lang-option').forEach(option => {
        option.addEventListener('click', function(e) {
            e.stopPropagation();
            const lang = this.getAttribute('data-lang');
            if (lang) setLanguage(lang);
            dropdown.classList.remove('open');
            trigger.setAttribute('aria-expanded', 'false');
            menu.setAttribute('aria-hidden', 'true');
        });
    });
    
    document.addEventListener('click', function() {
        dropdown.classList.remove('open');
        trigger.setAttribute('aria-expanded', 'false');
        menu.setAttribute('aria-hidden', 'true');
    });
}

// Initialize translations on page load
document.addEventListener('DOMContentLoaded', function() {
    applyTranslations();
    initLangDropdown();
    
    // Update password mismatch alert
    const registerForm = document.getElementById('registerForm');
    if (registerForm) {
        registerForm.addEventListener('submit', function(e) {
            e.preventDefault();
            
            const password = document.getElementById('password').value;
            const confirmPassword = document.getElementById('confirmPassword').value;
            
            if (password !== confirmPassword) {
                alert(translations[currentLanguage].register.passwordMismatch);
                return;
            }
            
            console.log('Registration form submitted');
        });
    }
});

